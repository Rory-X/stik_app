const TRADITIONAL_TO_SIMPLIFIED = Object.fromEntries(
  TRADITIONAL_TO_SIMPLIFIED_PAIRS,
) as Record<string, string>;

const SIMPLIFIED_TO_TRADITIONAL =
  TRADITIONAL_TO_SIMPLIFIED_PAIRS.reduce<Record<string, string>>(
    (acc, [traditional, simplified]) => {
      acc[simplified] ??= traditional;
      return acc;
    },
    {},
  );

export function resolveChineseScriptPreference(
  preference: string | null | undefined,
): ChineseScriptPreference {
  if (
    preference === "simplified" ||
    preference === "traditional" ||
    preference === "preserve"
  ) {
    return preference;
  }

  return "simplified";
}

export function normalizeChineseScript(
  text: string,
  preference: string | null | undefined,
): string {
  const resolved = resolveChineseScriptPreference(preference);
  if (!text || resolved === "preserve") return text;

  const phrases =
    resolved === "traditional"
      ? SIMPLIFIED_TO_TRADITIONAL_PHRASES
      : TRADITIONAL_TO_SIMPLIFIED_PHRASES;
  const map =
    resolved === "traditional"
      ? SIMPLIFIED_TO_TRADITIONAL
      : TRADITIONAL_TO_SIMPLIFIED;

  const phraseNormalized = phrases.reduce(
    (result, [from, to]) => result.split(from).join(to),
    text,
  );

  return Array.from(phraseNormalized, (char) => map[char] ?? char).join("");
}
